// ── File system ───────────────────────────────────────────────────────────────

/** Returns all file paths (not directories) under dir, recursively. */
function walkDir(dir: string): string[] {
  if (!fs.existsSync(dir)) return [];
  const results: string[] = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) results.push(...walkDir(full));
    else results.push(full);
  }
  return results;
}

/** True for essential/ paths that must never be synced (caches, binaries, .jar/.meta). */
function shouldSkipEssentialFile(fwdRelPath: string): boolean {
  const first = fwdRelPath.split('/')[0];
  return ESSENTIAL_EXCLUDE.has(first) ||
    fwdRelPath.endsWith('.jar') ||
    fwdRelPath.endsWith('.meta');
}

/**
 * Sync OVERRIDE_FOLDERS and INCLUDE_FILES from a modpack root into a versions-repo overrides/ dir.
 * Applies ESSENTIAL_EXCLUDE filtering for the essential/ folder.
 * Returns the number of files added, updated, or removed.
 */
function syncOverridesToRepo(root: string, overridesDir: string): number {
  let count = 0;

  for (const folder of OVERRIDE_FOLDERS) {
    const srcDir = path.join(root, folder);
    const destDir = path.join(overridesDir, folder);
    const isEssential = folder === 'essential';

    // Remove dest files that were deleted from src, or that are now excluded
    if (fs.existsSync(destDir)) {
      for (const destFile of walkDir(destDir)) {
        const rel = path.relative(destDir, destFile).replace(/\\/g, '/');
        const stale = !fs.existsSync(path.join(srcDir, rel.replace(/\//g, path.sep)));
        if (stale || (isEssential && shouldSkipEssentialFile(rel))) {
          try { fs.unlinkSync(destFile); count++; } catch {}
        }
      }
    }

    // Copy current src files into dest (skipping essential exclusions)
    if (fs.existsSync(srcDir)) {
      for (const srcFile of walkDir(srcDir)) {
        const rel = path.relative(srcDir, srcFile).replace(/\\/g, '/');
        if (isEssential && shouldSkipEssentialFile(rel)) continue;
        const destFile = path.join(destDir, rel.replace(/\//g, path.sep));
        fs.mkdirSync(path.dirname(destFile), { recursive: true });
        fs.copyFileSync(srcFile, destFile);
        count++;
      }
    }
  }

  // Sync root-level include_files directly into overrides/
  fs.mkdirSync(overridesDir, { recursive: true });
  for (const filename of INCLUDE_FILES) {
    const srcFile = path.join(root, filename);
    const destFile = path.join(overridesDir, filename);
    if (fs.existsSync(srcFile)) {
      fs.copyFileSync(srcFile, destFile);
      count++;
    } else if (fs.existsSync(destFile)) {
      try { fs.unlinkSync(destFile); count++; } catch {}
    }
  }

  return count;
}

// ── Modrinth hash cache (stored in versions repo, gitignored) ─────────────────

interface ModrinthLookupResult {
  found: boolean;
  slug?: string;
  title?: string;
  iconUrl?: string;
  downloadUrl?: string;
  fileSize?: number;
  filename: string;
}

function loadModrinthCache(versionsDir: string): Record<string, ModrinthLookupResult> {
  try {
    return JSON.parse(fs.readFileSync(path.join(versionsDir, '.modrinth_cache.json'), 'utf-8'));
  } catch {
    return {};
  }
}

function saveModrinthCache(versionsDir: string, cache: Record<string, ModrinthLookupResult>): void {
  try {
    fs.writeFileSync(
      path.join(versionsDir, '.modrinth_cache.json'),
      JSON.stringify(cache, null, 2),
      'utf-8',
    );
  } catch (e) {
    console.error('[modrinth-cache] save failed:', e);
  }
}

async function lookupModrinthHash(
  sha512: string,
  filename: string,
  cache: Record<string, ModrinthLookupResult>,
): Promise<ModrinthLookupResult> {
  if (sha512 in cache) return cache[sha512];

  const headers = { 'User-Agent': 'ORB-Modpack-Exporter/1.0' };
  try {
    const versionRes = await fetch(
      `https://api.modrinth.com/v2/version_file/${sha512}?algorithm=sha512`,
      { headers },
    );
    if (!versionRes.ok) {
      cache[sha512] = { found: false, filename };
      return cache[sha512];
    }
    const versionData: any = await versionRes.json();
    const projectId: string = versionData.project_id;
    const fileEntry =
      (versionData.files ?? []).find((f: any) => f.hashes?.sha512 === sha512) ??
      versionData.files?.[0];

    const projectRes = await fetch(`https://api.modrinth.com/v2/project/${projectId}`, { headers });
    let slug = projectId;
    let title = projectId;
    let iconUrl: string | undefined;
    if (projectRes.ok) {
      const proj: any = await projectRes.json();
      slug = proj.slug ?? projectId;
      title = proj.title ?? projectId;
      iconUrl = proj.icon_url ?? undefined;
    }

    cache[sha512] = {
      found: true,
      slug,
      title,
      iconUrl,
      downloadUrl: fileEntry?.url,
      fileSize: fileEntry?.size,
      filename,
    };
    return cache[sha512];
  } catch {
    cache[sha512] = { found: false, filename };
    return cache[sha512];
  }
}

