// ─── Auto-updater (electron-builder + GitHub Releases) ────────────────────────
// Release metadata is generated by electron-builder and published to GitHub
// Releases. Updates are downloaded in the background and installed on restart.

interface UpdateCheckResult {
  updateAvailable: boolean;
  version?: string;
  downloadUrl?: string;
  releaseNotes?: string;
}

function githubHeaders(extra: Record<string, string> = {}): Record<string, string> {
  // The releases repo is public — no Authorization needed.
  return {
    'User-Agent': 'ORB-Modpack-Exporter',
    ...extra,
  };
}

autoUpdater.autoDownload = false;
autoUpdater.autoInstallOnAppQuit = true;

let updaterConfigured = false;
let updateDownloaded = false;

function configureAutoUpdater(): void {
  if (updaterConfigured) return;
  updaterConfigured = true;

  autoUpdater.on('error', err => {
    console.warn('[updater] error:', err);
  });

  autoUpdater.on('update-downloaded', info => {
    updateDownloaded = true;
    dialog.showMessageBox({
      type: 'info',
      title: 'Update Ready',
      message: `ORB Modpack Exporter v${info.version} is ready to install.`,
      detail: 'Restart the app to finish installing the update.',
      buttons: ['Restart Now', 'Later'],
      defaultId: 0,
      cancelId: 1,
    }).then(({ response }) => {
      if (response === 0) autoUpdater.quitAndInstall(false, true);
    }).catch(err => console.warn('[updater] restart prompt failed:', err));
  });
}

async function checkForUpdate(): Promise<UpdateCheckResult> {
  if (!app.isPackaged) return { updateAvailable: false };
  configureAutoUpdater();

  try {
    const result = await autoUpdater.checkForUpdates();
    const info = result?.updateInfo;
    if (!info || info.version === app.getVersion()) {
      return { updateAvailable: false };
    }

    return {
      updateAvailable: true,
      version: info.version,
      downloadUrl: 'electron-updater',
      releaseNotes: typeof info.releaseNotes === 'string' ? info.releaseNotes : '',
    };
  } catch (e) {
    console.warn('[updater] check failed:', e);
    return { updateAvailable: false };
  }
}

async function installUpdate(_downloadUrl?: string): Promise<void> {
  if (!app.isPackaged) return;
  configureAutoUpdater();

  if (updateDownloaded) {
    autoUpdater.quitAndInstall(false, true);
    return;
  }

  await autoUpdater.downloadUpdate();
}

/**
 * Silent launch-time update check. If an update is found, prompts the user and, on
 * confirmation, downloads and installs it. Never throws.
 */
async function runLaunchUpdateCheck(): Promise<void> {
  const result = await checkForUpdate();
  if (!result.updateAvailable) return;

  const currentVersion = app.getVersion();
  const { response } = await dialog.showMessageBox({
    type: 'info',
    title: 'Update Available',
    message: `ORB Modpack Exporter v${result.version} is available.\n\nCurrent version: v${currentVersion}`,
    detail: result.releaseNotes?.substring(0, 500) || 'The update can be downloaded in the background.',
    buttons: ['Download in Background', 'Later'],
    defaultId: 0,
  });

  if (response === 0) {
    try {
      await installUpdate();
    } catch (e) {
      console.error('[updater] download failed:', e);
      dialog.showErrorBox('Update Failed', `Could not download the update.\n\n${String(e)}`);
    }
  }
}

