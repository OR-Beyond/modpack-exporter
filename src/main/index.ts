// ── Versions repo lifecycle ───────────────────────────────────────────────────

function ensureGitignore(versionsDir: string): void {
  const gitignorePath = path.join(versionsDir, '.gitignore');
  const required = ['.modrinth_cache.json', '.last_pull_state.json'];
  let content = '';
  try { content = fs.readFileSync(gitignorePath, 'utf-8'); } catch {}
  let changed = false;
  for (const entry of required) {
    if (!content.includes(entry)) {
      content += (content.length > 0 && !content.endsWith('\n') ? '\n' : '') + entry + '\n';
      changed = true;
    }
  }
  if (changed) fs.writeFileSync(gitignorePath, content, 'utf-8');
}

async function ensureVersionsRepo(versionsDir: string, token: string | null): Promise<void> {
  // 1. Verify the token actually has access to the versions repo *before* attempting
  //    a clone. Once the token is embedded in the clone URL, an auth failure comes
  //    back as an opaque git error; a direct API check gives a much clearer signal.
  if (token) {
    let accessCheck: Response | null = null;
    try {
      accessCheck = await fetch('https://api.github.com/repos/OR-Beyond/OR-Beyond-Versions', {
        headers: githubHeaders({
          Authorization: `Bearer ${token}`,
          Accept: 'application/vnd.github+json',
        }),
      });
    } catch {
      // Network failure on the check itself — fall through and let the clone
      // attempt below surface the real problem rather than guessing here.
    }
    if (accessCheck && (accessCheck.status === 401 || accessCheck.status === 404)) {
      throw new Error('Your GitHub account does not have access to the modpack repository. Contact a maintainer to be added.');
    }
  }

  const gitDir = path.join(versionsDir, '.git');

  // The versions dir can be left behind in a broken, half-initialized state (e.g. a
  // previous run created the folder but was interrupted before `git clone` populated
  // it). A folder without a .git subfolder isn't a usable repo — wipe it so the clone
  // below starts from a clean, nonexistent directory instead of silently no-op'ing.
  if (fs.existsSync(versionsDir) && !fs.existsSync(gitDir)) {
    fs.rmSync(versionsDir, { recursive: true, force: true });
  }

  fs.mkdirSync(versionsDir, { recursive: true });

  if (!fs.existsSync(gitDir)) {
    // 2. Clone.
    try {
      await gitProvider.clone(VERSIONS_REPO_URL, versionsDir, { ...makeGitAuth(), branch: 'main' });
    } catch (cloneErr: any) {
      const detail = cloneErr?.message || String(cloneErr);
      throw new Error(`git clone failed: ${detail}`);
    }
  } else {
    // Refresh remote URL, then pull.
    try { await gitProvider.setRemoteUrl(versionsDir, 'origin', VERSIONS_REPO_URL); } catch {}
    try {
      await gitProvider.fetch(versionsDir, { ...makeGitAuth() });
      await gitProvider.resetHard(versionsDir, 'origin/main');
    } catch (pullErr: any) {
      // No upstream yet (first push scenario) — not an error.
      if (
        !pullErr.message.includes("couldn't find remote ref") &&
        !pullErr.message.includes('no tracking information') &&
        !pullErr.message.includes('does not appear to be a git repository')
      ) {
        throw pullErr;
      }
    }
  }

  // 3. Belt-and-suspenders: .git exists but the working tree is somehow empty (e.g. a
  //    clone was interrupted after `.git` was written but before checkout completed).
  //    Force a fetch + hard reset to repopulate it. A genuinely fresh remote with no
  //    push yet (only a README, zero real content) will still have no manifest after
  //    this — that IS valid, and the caller already handles a missing manifest with
  //    its own clear message, so the fetch/reset failure here is swallowed.
  const manifestPath = path.join(versionsDir, 'modrinth.index.json');
  if (fs.existsSync(gitDir) && !fs.existsSync(manifestPath)) {
    try {
      await gitProvider.fetch(versionsDir, { ...makeGitAuth() });
      await gitProvider.resetHard(versionsDir, 'origin/main');
    } catch {}
  }
}

async function ensureGitIdentity(versionsDir: string, githubUser: string): Promise<void> {
  try {
    const email = await gitProvider.getConfig(versionsDir, 'user.email');
    if (email.trim()) return;
  } catch {}
  await gitProvider.setConfig(versionsDir, 'user.name', githubUser);
  await gitProvider.setConfig(versionsDir, 'user.email', `${githubUser}@users.noreply.github.com`);
}

